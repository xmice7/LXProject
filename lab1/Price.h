#pragma once

// Структура для зберігання ціни: гривні та копійки
struct Price {
    int     grn;  // гривні
    short   kop;  // копійки (0..99)

    // Додавання двох цін
    Price add(Price other) const;

    // Множення ціни на позитивне ціле число (кількість)
    Price multiply(int n) const;

    // Заокруглення копійок до найближчих 10
    // Правило: 1..4 -> 0, 5..9 -> 10 (монети до 10 коп. виведені з обігу)
    Price round() const;

    // Виведення ціни у консоль у форматі "X грн Y коп"
    void print() const;
};
